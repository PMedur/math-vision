package containers;

public class SizeContainer {

    private int width = 640;
    private int height = 480;

    public SizeContainer() {
    }

    public int getWidth() {
        return width;
    }

    public void setWidth(int width) {
        this.width = width;
    }

    public int getHeight() {
        return height;
    }

    public void setHeight(int height) {
        this.height = height;
    }
}
